#include <stdio.h>
#include <math.h>

int main() {
  printf("sqrt(16) %f\n", sqrt(16));
  printf("ceil(1.4) %f\n", ceil(1.4));
  printf("floor(25.5) %f\n", floor(25.5));
  printf("floor(25.99) %f\n", floor(25.99));
  printf("floor(25.2) %f\n", floor(25.2));
  printf("pow(4,3) %f", pow(4, 3));
  return 0;
}
